def greet():
    print("Hello World!")
    print("I come to conquer you...")
    print("With lovely pastries!")

greet()

def greet_with_food(food):
    print("Hello World!")
    print("I come to conquer you...")
    print(f"With {food}!")

greet_with_food("hot chocolate")
greet_with_food("ginger bread")
